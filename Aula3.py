# Laços de repetição + Listas
# Laços de repetição são maneiras de se executar o mesmo comando várias vezes

for palavra in range(1,10):
    print('Carregando')

# Estrutura de laços de repetição
'''
for item in coleção:
    espressão
'''
for item in range(1,21):
    print(item)

for item in range(1,20,4):
    print(item)

nomes = ['João','Carlos','Vitor','Otto']
for nome in nomes:
    print(nome)

animes = ['My hero academia','Jujutsu Kaisen','Chainsaw man']
for anime in animes:
    print(anime)

